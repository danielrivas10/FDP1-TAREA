#include <iostream>
using namespace std;

int main ()
{

int n;

cout <<"Por favor ingrese un número entero" << endl;
cin >> n;

switch (n){

case 1: 
cout << "Lunes" << endl;
break;

case 2: 
cout << "Martes" << endl;
break;

case 3: 
cout << "Miercoles" << endl;
break;

case 4: 
cout << "Jueves" << endl;
break;

case 5: 
cout << "Viernes" << endl;
break;

case 6: 
cout << "Sabado" << endl;
break;

case 7: 
cout << "Domingo" << endl;
break;

default:
cout <<"Por favor ingrese un numero del 1-7" << endl;

break;



}

return 0;


}








